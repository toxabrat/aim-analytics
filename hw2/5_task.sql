/*
Есть таблица Events с данными о поведении пользователей на сайте:

Поле
Описание
Тип
Примеры значений
user_client_id
Уникальный идентификатор клиента
int
123
date
Дата события по клиенту
date
2025-01-01
timestamp
Время события по клиенту
timestamp
18:02:00
attributes
Платформа, с которой заходил пользователь
varchar
ios, android
page_current_url
URL текущей страницы
varchar
/home, /catalog, /checkout
action_type
Тип действия пользователя
varchar
page_view, view, click
widget_id
Уникальный идентификатор элемента (виджета) на странице
int
456


Описание типов действий (action_type):
page_view — просмотр всей страницы.
view — пользователь увидел элемент страницы (например, баннер, кнопку и т.п.). Событие отправляется только тогда, когда элемент оказался в зоне видимости на экране пользователя.
click — пользователь нажал на элемент. Событие отправляется только если элемент был видим и пользователь кликнул на него. Пока клиент не доскроллит до элемента и не нажмёт, событие не будет зафиксировано.

Рассматривается поведение пользователей на странице каталога: /catalog.
На этой странице расположены два виджета:
widget_id = 7700 — находится в верхней части страницы, сразу попадает в зону видимости при загрузке.
widget_id = 7788 — находится в нижней части страницы, становится видимым только после прокрутки вниз.
Построить SQL-скрипт, который будет считать конверсию из события view виджета 7700 в событие click по нему с разбивкой по дням (date).

*/

SELECT COALESCE(
        COUNT(DISTINCT e.user_client_id) FILTER (
            WHERE e.action_type = 'click'
        ) * 1.0 / NULLIF(
            COUNT(DISTINCT e.user_client_id) FILTER (
                WHERE e.action_type = 'view'
            ),
            0
        ),
        0
    ) AS conversion,
    e.date AS event_date
FROM events e
WHERE e.widget_id = 7700
    AND e.page_current_url = '/catalog'
GROUP BY e.date
ORDER BY e.date;