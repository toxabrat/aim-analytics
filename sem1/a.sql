DROP TABLE IF EXISTS opliuta.users;
DROP TABLE IF EXISTS opliuta.regs;
DROP TABLE IF EXISTS opliuta.payments;

CREATE SCHEMA opliuta;

-- Создание таблицы users
CREATE TABLE opliuta.users (
    user_id INTEGER,
    city VARCHAR
);

-- Создание таблицы regs
CREATE TABLE opliuta.regs (
    user_id INTEGER,
    reg_date DATE
);

-- Создание таблицы payments с правильной точностью TIMESTAMP(3) для секунд
CREATE TABLE opliuta.payments (
    payment_id INTEGER,
    user_id INTEGER,
    amount DECIMAL(10,2),
    payment_dt TIMESTAMP(6)
);


